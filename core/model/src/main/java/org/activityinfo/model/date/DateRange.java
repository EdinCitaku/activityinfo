import java.io.Serializable;
import java.util.Date;

/**
 * @author yuriyz on 02/05/2015.
 */
public class DateRange implements Serializable {
    private Date start;
    private Date end;

    public DateRange() {
    }

    public DateRange(Date start, Date end) {
        this.start = start;
        this.end = end;
    }

    public Date getStart() {
        return start;
    }

    public void setStart(Date start) {
        this.start = start;
    }

    public Date getEnd() {
        return end;
    }

    public void setEnd(Date end) {
        this.end = end;
    }

    public Date midDate() {
        long midDate = (start.getTime() + end.getTime()) / 2;
        return new Date(midDate);
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        DateRange dateRange = (DateRange) o;
        if (end != null ? !end.equals(dateRange.end) : dateRange.end != null) return false;
        if (start != null ? !start.equals(dateRange.start) : dateRange.start != null) return false;
        return true;
    }

    @Override
    public int hashCode() {
        int result = start != null ? start.hashCode() : 0;
        result = 31 * result + (end != null ? end.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "DateRange{" +
                "start=" + start +
                ", end=" + end +
                '}';
    }
}